package day2.basicexamples;

public class SwitchCase {

	public static void main(String[] args) {
		int value =3;
		switch(value)
		{
		case 3:
			System.out.println("I am case 3");
		break;
		case 5:
		
			System.out.println("I am case 5");
			break;
			default:
				System.out.println("I am case default");
				break;
				
		}

	}

}
